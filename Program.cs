namespace ConsoleApp22
{
    using System;

    public class Порівняння<T> where T : IComparable<T>
    {
        private T значення1;
        private T значення2;

        // Конструктор, що приймає два значення
        public Порівняння(T значення1, T значення2)
        {
            this.значення1 = значення1;
            this.значення2 = значення2;
        }

        // Метод для знаходження меншого з двох чисел
        public T ЗнайтиМенше()
        {
            return значення1.CompareTo(значення2) < 0 ? значення1 : значення2;
        }
    }

    class Program
    {
        static void Main(string[] args)
        {
            // Приклад використання з цілими числами
            Порівняння<int> порівнянняInt = new Порівняння<int>(5, 10);
            Console.WriteLine($"Менше з чисел: {порівнянняInt.ЗнайтиМенше()}");

            // Приклад використання з дійсними числами
            Порівняння<double> порівнянняDouble = new Порівняння<double>(7.5, 3.2);
            Console.WriteLine($"Менше з чисел: {порівнянняDouble.ЗнайтиМенше()}");

            // Приклад використання з рядками
            Порівняння<string> порівнянняString = new Порівняння<string>("яблуко", "ананас");
            Console.WriteLine($"Менший за алфавітом рядок: {порівнянняString.ЗнайтиМенше()}");
        }
    }

}
